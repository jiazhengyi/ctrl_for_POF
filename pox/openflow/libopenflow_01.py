def _unpack_instrucion(b,length,instructionNum,offset=0):   #add by milktank
  """
    Parses instrucions from a buffer
    b is a buffer (bytes)
    offset, if specified, is where in b to start decoding
    returns (next_offset, [instrucions])
  """
  if ( len( b ) - offset) < length: raise UnderrunError
  instructions = []
  end = length + offset
  numcount=0
  while offset < end:
    (t, l) = struct.unpack_from ( "!HH", b , offset)
    if ( len( b ) - offset) < l: raise UnderrunError
    a = _instruction_type_to_class.get (t)
    #print (a)
    if a is None :
      # Use generic action header for unknown type
      a = None
      #print (t,l)
      #print ("Unknow instruction type")
      break
    else:
      a = a()
    #print (t,l, offset)
    a.unpack ( b[ offset:offset + l ])
    assert len (a) == l
    instructions.append (a)
    #print (a)
    #offset += l  
    offset += ofp_instruction._MAX_LENGTH
    numcount+=1 
  offset +=(OFP_MAX_INSTRUCTION_NUM - instructionNum)*ofp_instruction._MAX_LENGTH
  return ( offset, instructions)

